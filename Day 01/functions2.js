
// Functions in JS:
// -Functions in JS are first class citizens
// -It means you can 
// -create a function,
// -modify a function,
// -Use function as an argument,
// -You can return a function as a function,
// -You can assign a function to a variable.


// Function Declaration
// Function Statement is as same as Function Declaration
function a() {
    console.log("A")
}
a();

// Anonymous Function
// A function without a name is anonymous function
// In other words a functions declared to a variable is an Anonymous Function


// Named Function Expression:
// A function expression which is named 
var x = function xyz() {
    console.log(xyz)
} 
xyz();

