// Block in JS is also known as compound statement
// It is defined by curly braces {}
// It is used to combine multiple JS statements in one group
{
    var a = 100;
    let b = 20;             //Here, let & const can only accessed inside block{}
    const c = 30; 
}

// Here, var is stored in Global memory space,
// And let and const are Block Scoped which means they're stored in 
// separate memory space which is reserved for this block.
